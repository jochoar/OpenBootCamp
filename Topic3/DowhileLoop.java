public class DowhileLoop {
    //For the Do While loop, you'll need to create the same structure as the While loop, but it only needs to be executed once.
    public static void main(String[] args) {
        int DowhileNumber = 0;

        do {
            System.out.println(DowhileNumber);
            DowhileNumber = DowhileNumber + 1;
        } while (DowhileNumber < 1);
    }
}
